package org.gitmining.bean;

public class UserScore {
	int user_id;
	int efficiency_score;
	int quantity_score;
	int total_score;
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public int getEfficiency_score() {
		return efficiency_score;
	}
	public void setEfficiency_score(int efficiency_score) {
		this.efficiency_score = efficiency_score;
	}
	public int getQuantity_score() {
		return quantity_score;
	}
	public void setQuantity_score(int quantity_score) {
		this.quantity_score = quantity_score;
	}
	public int getTotal_score() {
		return total_score;
	}
	public void setTotal_score(int total_score) {
		this.total_score = total_score;
	}
	
}
